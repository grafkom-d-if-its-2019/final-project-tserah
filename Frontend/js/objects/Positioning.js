class Positioning {
    /**
     * 
     * @param {Number} x 
     * @param {Number} y 
     * @param {Number} orientation (degree)
     * @param {Number} speed 
     */
    constructor(x, y, orientation, speed) {
        this.x = x;
        this.y = y;
        this.orientation = orientation;
        this.speed = speed;
    }
}